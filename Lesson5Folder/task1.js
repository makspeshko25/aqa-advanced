const averagGrade = 81;
if (averagGrade < 60) {
  console.log('Незадовільно');
} else if (averagGrade >= 60 && averagGrade <= 70) {
  console.log('Задовільно');
} else if (averagGrade >= 71 && averagGrade <= 80) {
  console.log('Добре');
} else if (averagGrade >= 81 && averagGrade <= 90) {
  console.log('Дуже добре');
} else if (averagGrade >= 91 && averagGrade <= 100) {
  console.log('Відмінно');
}
